"""Детектор режима: определяет search, consultation или off-topic по тексту запроса."""

import re


# Фиксированный ответ для off-topic запросов
OFF_TOPIC_ANSWER = (
    "Извините, я могу помогать только с вопросами о гитарах и музыкальном оборудовании. "
    "Попробуйте спросить что-то вроде: «подбери стратокастер до 500$» или «что такое хамбакер?»."
)

# Паттерны off-topic — НЕ про гитары
_OFF_TOPIC_PATTERNS = [
    # Программирование
    "напиши код", "сортировк", "функци[яю]", "python", "javascript",
    "программ", "алгоритм", "массив", "переменн", "цикл",
    # Математика
    "реши уравнен", "посчитай", "интеграл", "производн",
    "математик", "формула", "теорем",
    # Бытовое
    "какая погода", "рецепт", "борщ", "новости", "прогноз погод",
    "как приготовить", "сколько времени",
]

# Сигналы режима search — глаголы действия и намерение купить/подобрать
_SEARCH_KEYWORDS = [
    "подбери", "найди", "покажи варианты", "покажи вариант",
    "что купить", "посоветуй модель", "подборк",
    "подскажи .{0,30}до \\d",  # "подскажи Telecaster до 1500$"
    "сделай подборку",
]

# Паттерны бюджета — указание цены = намерение искать
_BUDGET_PATTERNS = [
    r"до \d+\s*\$",
    r"до \d+\s*долл",
    r"\d+\s*тысяч",
    r"бюджет",
    r"в пределах \d+",
]

# Ключевые слова, сигнализирующие намерение купить (в сочетании с контекстом)
_PURCHASE_INTENT = [
    "нужна", "нужен", "нужны",
    "хочу .{0,20}(купить|взять|приобрести|заказать)",
    "ищу",
]

# Паттерны пере-поиска — пользователь хочет увидеть предыдущие результаты ещё раз.
# Срабатывают только при has_previous_search=True (есть поиск в истории сессии).
_RESEARCH_PATTERNS = [
    r"ещ[её]\s*раз",
    r"еще\s*раз",
    r"покажи.{0,30}ссылк",
    r"покажи.{0,30}результат",
    r"покажи.{0,30}вариант",
    r"повтори",
    r"заново",
    r"те\s*же",
    r"такие\s*же",
    r"снова\s*(покажи|найди|поиск|подбери)",
    r"(ссылк|вариант).{0,20}(поиск|найд|подбор)",
]

# Сигналы режима consultation — вопросы про теорию, сравнения, обучение
_CONSULTATION_KEYWORDS = [
    "что лучше", "в чем разница", "в чём разница",
    "как влияет", "какой звук",
    "чем отличаются", "чем отличается",
    "почему", "как выбрать", "с чего начать",
    "что важнее", "как .{0,10} влия",
    "что значит", "что такое",
    "расскажи о ", "объясни",
]


def detect_mode(text: str, has_previous_search: bool = False) -> str:
    """Определяет режим работы агента по тексту запроса.

    Args:
        text: Текст запроса пользователя.
        has_previous_search: Есть ли в текущей сессии предыдущий поиск.
            Если True, дополнительно проверяются паттерны пере-поиска.

    Возвращает:
        "off_topic" — если запрос НЕ про гитары (программирование, математика, бытовое)
        "search" — если пользователь хочет подобрать конкретный инструмент
        "consultation" — если пользователь хочет разобраться в теории
    """
    if not text or not text.strip():
        return "consultation"

    lower = text.lower().strip()

    # Off-topic проверка — ДО всех остальных (даже при has_previous_search)
    if _match_any(lower, _OFF_TOPIC_PATTERNS):
        return "off_topic"

    has_search = _match_any(lower, _SEARCH_KEYWORDS)
    has_budget = _match_any(lower, _BUDGET_PATTERNS)
    has_purchase = _match_any(lower, _PURCHASE_INTENT)
    has_consultation = _match_any(lower, _CONSULTATION_KEYWORDS)

    # Бюджет или покажи варианты — почти наверняка search
    if has_budget:
        return "search"

    # Явный search-сигнал
    if has_search:
        return "search"

    # Намерение купить (даже если есть consultation-сигналы — приоритет search)
    if has_purchase:
        return "search"

    # Пере-поиск: пользователь ссылается на предыдущие результаты
    if has_previous_search and _match_any(lower, _RESEARCH_PATTERNS):
        return "search"

    # Consultation-сигналы без search-сигналов
    if has_consultation:
        return "consultation"

    # По умолчанию — consultation (безопаснее спросить, чем искать)
    return "consultation"


def _match_any(text: str, patterns: list[str]) -> bool:
    """Проверяет, содержит ли текст хотя бы один из паттернов."""
    for pattern in patterns:
        if re.search(pattern, text):
            return True
    return False
